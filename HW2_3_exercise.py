from pathlib import Path  # –Ü–º–ø–æ—Ä—Ç –∫–ª–∞—Å—É Path –¥–ª—è —Ä–æ–±–æ—Ç–∏ –∑ —Ñ–∞–π–ª–æ–≤–∏–º–∏ —à–ª—è—Ö–∞–º–∏
import logging  # –Ü–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è logging –¥–ª—è —Ä–æ–±–æ—Ç–∏ –∑ –ª–æ–≥—É–≤–∞–Ω–Ω—è–º –ø–æ–¥—ñ–π
from colorama import init, Fore, Style  # –î–ª—è –∫–æ–ª—å–æ—Ä–æ–≤–æ–≥–æ –≤–∏–≤–µ–¥–µ–Ω–Ω—è

# –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑—É—î–º–æ Colorama –¥–ª—è –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è –∫–æ–ª—å–æ—Ä–æ–≤–æ–≥–æ —Ç–µ–∫—Å—Ç—É –≤ –∫–æ–Ω—Å–æ–ª—ñ
init(autoreset=True)

# –ù–∞–ª–∞—à—Ç–æ–≤—É—î–º–æ –ª–æ–≥—É–≤–∞–Ω–Ω—è –¥–ª—è –≤—ñ–¥—Å—Ç–µ–∂–µ–Ω–Ω—è –ø–æ—Ç–µ–Ω—Ü—ñ–π–Ω–∏—Ö –ø–æ–º–∏–ª–æ–∫ —ñ –ø–æ–¥—ñ–π –≤ –∫–æ–¥—ñ
logger = logging.getLogger(__name__)
logging.basicConfig(level=logging.DEBUG, format='%(levelname)s: %(message)s')

# –§—É–Ω–∫—Ü—ñ—è —Å—Ç–≤–æ—Ä—é—î —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π –Ω–∞ –æ—Å–Ω–æ–≤—ñ –∑–∞–¥–∞–Ω–æ–≥–æ —à–ª—è—Ö—É


def create_directory_structure(base_path):
    """
    –°—Ç–≤–æ—Ä—é—î —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π –≤–∫–ª—é—á–∞—é—á–∏ –ø–∞–ø–∫—É 'picture' —Ç–∞ –≤–∫–ª–∞–¥–µ–Ω—É 'Logo'.
    """
    try:
        # –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –≥–æ–ª–æ–≤–Ω–æ—ó –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'picture'
        picture_path = base_path / 'picture'
        # exist_ok=True –∑–∞–ø–æ–±—ñ–≥–∞—î –ø–æ–º–∏–ª—Ü—ñ, —è–∫—â–æ –ø–∞–ø–∫–∞ –≤–∂–µ —ñ—Å–Ω—É—î
        picture_path.mkdir(parents=True, exist_ok=True)
        logger.info(f"–°—Ç–≤–æ—Ä–µ–Ω–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—é: {picture_path}")

        # –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –ø—ñ–¥–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'Logo' –≤—Å–µ—Ä–µ–¥–∏–Ω—ñ 'picture'
        logo_path = picture_path / 'Logo'
        logo_path.mkdir(exist_ok=True)
        logger.info(f"–°—Ç–≤–æ—Ä–µ–Ω–æ –ø—ñ–¥–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—é: {logo_path}")

        # –°—Ç–≤–æ—Ä–µ–Ω–Ω—è —Ñ–∞–π–ª—ñ–≤ —É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'picture'
        # .touch() —Å—Ç–≤–æ—Ä—é—î —Ñ–∞–π–ª, —è–∫—â–æ –≤—ñ–Ω –Ω–µ —ñ—Å–Ω—É—î
        (picture_path / "bot-icon.png").touch()
        (picture_path / "mongodb.jpg").touch()
        logger.info("–°—Ç–≤–æ—Ä–µ–Ω–æ —Ñ–∞–π–ª–∏ —É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'picture'.")

        # –°—Ç–≤–æ—Ä–µ–Ω–Ω—è —Ñ–∞–π–ª—ñ–≤ —É –ø—ñ–¥–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'Logo'
        (logo_path / "IBM+Logo.png").touch()
        (logo_path / "ibm.svg").touch()
        (logo_path / "logo-tm.png").touch()
        logger.info("–°—Ç–≤–æ—Ä–µ–Ω–æ —Ñ–∞–π–ª–∏ —É –ø—ñ–¥–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'Logo'.")

    except Exception as e:
        logger.exception("–ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ —Å—Ç–≤–æ—Ä–µ–Ω–Ω—ñ —Å—Ç—Ä—É–∫—Ç—É—Ä–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π: %s", e)

# –§—É–Ω–∫—Ü—ñ—è –≤—ñ–∑—É–∞–ª—ñ–∑—É—î —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –≤ –∫–æ–Ω—Å–æ–ª—ñ –∑ –∫–æ–ª—å–æ—Ä–∞–º–∏


def visualize_directory_structure(directory_path):
    """
    –í—ñ–∑—É–∞–ª—ñ–∑—É—î —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó 'picture' —É –∫–æ–Ω—Å–æ–ª—ñ –∑ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è–º –∫–æ–ª—å–æ—Ä–æ–≤–æ–≥–æ —Ç–µ–∫—Å—Ç—É.
    """
    try:
        path = Path(directory_path)
        # –í–∏–≤–æ–¥–∏–º–æ –Ω–∞–∑–≤—É –≥–æ–ª–æ–≤–Ω–æ—ó –ø–∞–ø–∫–∏ —Å–∏–Ω—ñ–º –∫–æ–ª—å–æ—Ä–æ–º
        print(Fore.BLUE + f"üì¶{path.name}/")
        for item in sorted(path.iterdir()):  # –ü–µ—Ä–µ–±–∏—Ä–∞—î–º–æ –≤—Å—ñ –µ–ª–µ–º–µ–Ω—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
            if item.is_dir():  # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —î –ø–∞–ø–∫–æ—é
                # –í–∏–≤–æ–¥–∏–º–æ –Ω–∞–∑–≤—É –ø–∞–ø–∫–∏ —Å–∏–Ω—ñ–º –∫–æ–ª—å–æ—Ä–æ–º
                print(Fore.BLUE + f" ‚î£ üìÇ{item.name}/")
                for subitem in sorted(item.iterdir()):  # –ü–µ—Ä–µ–±–∏—Ä–∞—î–º–æ –≤–º—ñ—Å—Ç –ø–∞–ø–∫–∏
                    # –í–∏–≤–æ–¥–∏–º–æ –Ω–∞–∑–≤–∏ —Ñ–∞–π–ª—ñ–≤ –∑–µ–ª–µ–Ω–∏–º –∫–æ–ª—å–æ—Ä–æ–º
                    print(Fore.GREEN + f" ‚îÉ ‚î£ üìú{subitem.name}")
                print(Fore.BLUE + f" ‚îÉ ‚îó")  # –ó–∞–∫—Ä–∏–≤–∞—î–º–æ –±–ª–æ–∫ –ø–∞–ø–∫–∏
            else:
                # –í–∏–≤–æ–¥–∏–º–æ –Ω–∞–∑–≤–∏ —Ñ–∞–π–ª—ñ–≤ –∑–µ–ª–µ–Ω–∏–º –∫–æ–ª—å–æ—Ä–æ–º
                print(Fore.GREEN + f" ‚îó üìú{item.name}")

    except Exception as e:
        logger.exception(
            "–ü–æ–º–∏–ª–∫–∞ –ø—ñ–¥ —á–∞—Å –≤—ñ–∑—É–∞–ª—ñ–∑–∞—Ü—ñ—ó —Å—Ç—Ä—É–∫—Ç—É—Ä–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π: %s", e)


# –®–ª—è—Ö –¥–æ —Ñ–∞–π–ª–∞ —Å–∫—Ä–∏–ø—Ç–∞, —â–æ –º—ñ—Å—Ç–∏—Ç—å —Ü–µ–π –∫–æ–¥
script_path = Path(__file__).resolve()
base_path = script_path.parent

# –°—Ç–≤–æ—Ä—é—î–º–æ —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó —Ç–∞ –≤—ñ–∑—É–∞–ª—ñ–∑—É—î–º–æ —ó—ó
create_directory_structure(base_path)
visualize_directory_structure(base_path / 'picture')
